# Library -----------------------------------------------------------------
#------------------------------------------------------------------------------#

library(rbenchmark)
library(matrixStats)
library()

#------------------------------------------------------------------------------#
# Functions ---------------------------------------------------------------
#------------------------------------------------------------------------------#


#------------------------------------------------------------------------------#
# Code -------------------------------------------------------------------
#------------------------------------------------------------------------------#

a <- seq(0,999,1)
sum(a[which(a %% 3 == 0 | a %% 5 ==0)])



A <- 0:10^6


A[which(A %% 3 == 0 | A %% 5 == 0)][78634]

B <- A[which(A %% 3 == 0 | A %% 5 == 0)]
sum(B)




test <- benchmark("Algo 1" = {
                  A <- seq(1,10^6,1)
                  
                  sum(A[which(A %% 3 == 0 | A %% 5 == 0)])
                },
                
                "Algo 2" = {
                  A <- 1:10^6
                  
                  sum(A[which(A %% 3 == 0 | A %% 5 == 0)])
                  
                },
                
                "Algo 3" = {
                  A <- 1:10^6

                  sum(A[setdiff(union(which(A %% 3 == 0), which(A %% 5 == 0)),
                                intersect(which(A %% 3 == 0), which(A %% 5 == 0)))])
                },

                "Algo 4" = {
                  A <- 1:10^6
                  
                  B <- A[which(A %% 3 == 0)]
                  
                  C <- setdiff(A,B)
                  
                  C <- C[which(C %% 5 == 0)]
                  
                  sum(B) + sum (C)
                },
                replications = 10)

test$meanTime <- test$elapsed/test$replications
test
